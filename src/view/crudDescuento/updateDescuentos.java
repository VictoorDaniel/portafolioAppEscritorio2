    private void verificaDatos(){
        if(    txtMinPuntosDescuentoMod.getText().trim().length() != 0 
            && txtMaxPuntosDescuentoMod.getText().trim().length() != 0
            && txtTopeDescuentoMod.getText().trim().length() != 0
            && txtPorcentajeDescuentoMod.getText().trim().length() != 0
           ) {
            //si los datos son correctamente insertados se ejecuta funcion para que guarde
            //agregaDesucento()
            try {
                modificar();
                //agregaDesucento();
            } catch (SQLException ex) {
                Logger.getLogger(createDescuentos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            }
        else {
            JOptionPane.showMessageDialog(null, "Debe insertar todos los datos");
        }
    }
    
    //metodo para modificar 
   public void modificar() throws SQLException {//throws SQLException{
       
        Descuento dscto = new Descuento();
        CrudDescuento controllerDscto = new CrudDescuento();
        
        dscto.setIdDescuento(parseInt(txtIdDescuentoMod.getText()));
        
        dscto.setMinPuntos(parseInt(txtMinPuntosDescuentoMod.getText()));
        dscto.setMaxPuntos(parseInt(txtMaxPuntosDescuentoMod.getText()));
        dscto.setPorcentajeDescuento(parseInt(txtPorcentajeDescuentoMod.getText()));
        dscto.setTopeDescuento(parseInt(txtTopeDescuentoMod.getText())); 
        
        controllerDscto.modificarDescuento(dscto);
        
    }

